export default function AuthVisualPanel({
  variant = "login",
  title,
  caption,
  compact = false,
}) {
  const variants = {
    login: {
      eyebrow: "Portal Preview",
      title: title || "Vehicles, bookings, alerts, and support in one workspace.",
      caption:
        caption || "A compact, app-like visual that feels right on desktop, tablet, and mobile.",
      preview: <DashboardPreview />,
    },
    signup: {
      eyebrow: "Quick Onboarding",
      title: title || "Register once, then manage services from a cleaner dashboard.",
      caption:
        caption || "The visual replaces long filler content so signup stays lighter on smaller screens.",
      preview: <SignupPreview />,
    },
    recovery: {
      eyebrow: "Secure Recovery",
      title: title || "Account recovery with the same customer data kept intact.",
      caption:
        caption || "A focused recovery screen that reduces vertical clutter on phones and tablets.",
      preview: <RecoveryPreview />,
    },
  };

  const activeVariant = variants[variant] || variants.login;

  return (
    <div className={`auth-visual auth-visual--${variant} ${compact ? "auth-visual--compact" : ""}`.trim()}>
      {!compact ? (
        <div className="auth-visual__copy">
          <span className="auth-visual__eyebrow">{activeVariant.eyebrow}</span>
          <strong>{activeVariant.title}</strong>
          <p>{activeVariant.caption}</p>
        </div>
      ) : null}

      <div className="auth-visual__art">{activeVariant.preview}</div>
    </div>
  );
}
